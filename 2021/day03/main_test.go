package main

import (
	"testing"
)

var example = `00100
11110
10110
10111
10101
01111
00111
11100
10000
11001
00010
01010`

func Test_part1(t *testing.T) {
	tests := []struct {
		name  string
		input string
		want  int
	}{
		{
			name:  "example",
			input: example,
			want:  198,
		},
		{
			name:  "actual",
			input: input,
			want:  3320834,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := part1(tt.input); got != tt.want {
				t.Errorf("part1() = %v, want %v", got, tt.want)
			}
		})
	}
}

